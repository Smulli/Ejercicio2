public class VariableChar {
	public static void main (String args[]) {
		int N = 3;
		double A = 4.56;
		char caracter = 'a';
		
		System.out.println(N+A); //resultado de la suma.
		System.out.println(N-A); //resultado de la resta.
		System.out.print((int)caracter); //saco el valor numérico de 'a'.
	}
	
}

